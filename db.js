const db = new Dexie("sskg_db");

db.version(1).stores({
    customers: '++id, name, phone, createdAt, balance',
    katha_entries: '++id, customerId, type, createdAt, isAutoGenerated',
    payments: '++id, customerId, createdAt',
    daily_configs: '++id, customerId, isActive, scheduledTime',
    history_log: '++id, eventType, customerId, referenceId, createdAt'
});

// Wrapper functions for database operations
const dbOps = {
    // Customers
    async addCustomer(name, phone) {
        return await db.customers.add({
            name,
            phone,
            createdAt: Date.now(),
            balance: 0
        });
    },
    async getCustomers() {
        return await db.customers.orderBy('name').toArray();
    },
    async getCustomer(id) {
        return await db.customers.get(id);
    },
    async updateCustomerBalance(customerId, amountChange) {
        const customer = await db.customers.get(customerId);
        if (customer) {
            customer.balance += amountChange;
            await db.customers.put(customer);
        }
        return customer;
    },

    // Katha Entries
    async addKathaEntry(customerId, items, totalAmount, type = 'normal', isAutoGenerated = false) {
        return await db.transaction('rw', db.katha_entries, db.customers, db.history_log, async () => {
            const entry = {
                customerId,
                type,
                items,
                totalAmount,
                createdAt: Date.now(),
                isAutoGenerated,
                editHistory: []
            };
            const entryId = await db.katha_entries.add(entry);
            const customer = await dbOps.updateCustomerBalance(customerId, totalAmount);
            
            await db.history_log.add({
                eventType: 'katha_added',
                customerId,
                referenceId: entryId,
                description: `Added ${type} katha entry of ₹${totalAmount}`,
                amount: totalAmount,
                createdAt: Date.now(),
                metadata: {}
            });
            
            return entryId;
        });
    },
    
    async getKathaEntriesByCustomer(customerId) {
        return await db.katha_entries.where('customerId').equals(customerId).reverse().sortBy('createdAt');
    },
    
    async getTodayKathaEntries() {
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        return await db.katha_entries.where('createdAt').aboveOrEqual(today.getTime()).toArray();
    },
    
    async getThisMonthKathaAmount() {
        const today = new Date();
        today.setDate(1);
        today.setHours(0, 0, 0, 0);
        const entries = await db.katha_entries.where('createdAt').aboveOrEqual(today.getTime()).toArray();
        return entries.reduce((sum, entry) => sum + entry.totalAmount, 0);
    },

    // Payments
    async addPayment(customerId, amount, paymentType = 'full') {
        return await db.transaction('rw', db.payments, db.customers, db.history_log, async () => {
            const customer = await db.customers.get(customerId);
            if (!customer) throw new Error("Customer not found");
            
            const balanceBefore = customer.balance;
            const balanceAfter = customer.balance - amount;
            
            const payment = {
                customerId,
                amount,
                paymentType,
                balanceBefore,
                balanceAfter,
                createdAt: Date.now(),
                editHistory: []
            };
            
            const paymentId = await db.payments.add(payment);
            await dbOps.updateCustomerBalance(customerId, -amount);
            
            await db.history_log.add({
                eventType: 'payment_received',
                customerId,
                referenceId: paymentId,
                description: `Received ${paymentType} payment of ₹${amount}`,
                amount: amount,
                createdAt: Date.now(),
                metadata: {}
            });
            
            return paymentId;
        });
    },

    async editPayment(paymentId, newAmount, reason) {
        return await db.transaction('rw', db.payments, db.customers, db.history_log, async () => {
            const payment = await db.payments.get(paymentId);
            if (!payment) throw new Error("Payment not found");
            
            const diff = newAmount - payment.amount;
            
            payment.editHistory.push({
                oldAmount: payment.amount,
                newAmount: newAmount,
                reason,
                date: Date.now()
            });
            
            payment.amount = newAmount;
            payment.balanceAfter = payment.balanceBefore - newAmount;
            
            await db.payments.put(payment);
            await dbOps.updateCustomerBalance(payment.customerId, -diff);
            
            await db.history_log.add({
                eventType: 'payment_edited',
                customerId: payment.customerId,
                referenceId: paymentId,
                description: `Edited payment to ₹${newAmount} (${reason || 'Quick edit'})`,
                amount: newAmount,
                createdAt: Date.now(),
                metadata: { oldAmount: payment.editHistory[payment.editHistory.length-1].oldAmount }
            });
            
            return paymentId;
        });
    },

    async getTodayPayments() {
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        return await db.payments.where('createdAt').aboveOrEqual(today.getTime()).toArray();
    },

    // Daily Configs
    async setDailyConfig(customerId, items, scheduledTime, isActive = true) {
        // Check if config exists
        const existing = await db.daily_configs.where('customerId').equals(customerId).first();
        if (existing) {
            return await db.daily_configs.update(existing.id, { items, scheduledTime, isActive });
        } else {
            return await db.daily_configs.add({
                customerId,
                items,
                scheduledTime,
                isActive,
                lastTriggeredDate: null
            });
        }
    },
    
    async getDailyConfigs() {
        return await db.daily_configs.toArray();
    },
    
    async getDailyConfigForCustomer(customerId) {
        return await db.daily_configs.where('customerId').equals(customerId).first();
    },
    
    async updateDailyConfigLastTriggered(id, dateString) {
        return await db.daily_configs.update(id, { lastTriggeredDate: dateString });
    },

    // History Log
    async getHistoryLog(limit = 100) {
        return await db.history_log.orderBy('createdAt').reverse().limit(limit).toArray();
    },
    
    async getHistoryLogByCustomer(customerId) {
        return await db.history_log.where('customerId').equals(customerId).reverse().sortBy('createdAt');
    },

    // Stats
    async getStats() {
        const todayEntries = await this.getTodayKathaEntries();
        const todayPayments = await this.getTodayPayments();
        const monthAmount = await this.getThisMonthKathaAmount();
        
        const customers = await this.getCustomers();
        let highestCustomer = { name: '-', balance: 0 };
        for (let c of customers) {
            if (c.balance > highestCustomer.balance) {
                highestCustomer = c;
            }
        }
        
        return {
            kathasToday: todayEntries.length,
            paidToday: todayPayments.length,
            monthTotal: monthAmount,
            highestKatha: highestCustomer
        };
    },
    
    async getWeeklyChartData() {
        const today = new Date();
        today.setHours(0, 0, 0, 0);

        const thisWeekData = [0, 0, 0, 0, 0, 0, 0];
        const lastWeekData = [0, 0, 0, 0, 0, 0, 0];

        // Fetch last 14 days of entries
        const fourteenDaysAgo = new Date(today.getTime() - (13 * 24 * 60 * 60 * 1000));
        const entries = await db.katha_entries.where('createdAt').aboveOrEqual(fourteenDaysAgo.getTime()).toArray();

        entries.forEach(e => {
            const entryDate = new Date(e.createdAt);
            entryDate.setHours(0, 0, 0, 0);
            const diffTime = today.getTime() - entryDate.getTime();
            const diffDays = Math.floor(diffTime / (24 * 60 * 60 * 1000));
            
            if (diffDays >= 0 && diffDays <= 6) {
                thisWeekData[6 - diffDays] += e.totalAmount;
            } else if (diffDays >= 7 && diffDays <= 13) {
                lastWeekData[13 - diffDays] += e.totalAmount;
            }
        });

        const labels = [];
        for (let i = 6; i >= 0; i--) {
            const d = new Date(today.getTime() - (i * 24 * 60 * 60 * 1000));
            labels.push(d.toLocaleDateString('en-US', { weekday: 'short' }));
        }

        return { labels, thisWeekData, lastWeekData };
    },

    async deleteOldHistory() {
        const ninetyDaysAgo = Date.now() - (90 * 24 * 60 * 60 * 1000);
        const oldLogs = await db.history_log.where('createdAt').below(ninetyDaysAgo).toArray();
        if(oldLogs.length > 0) {
            const ids = oldLogs.map(l => l.id);
            await db.history_log.bulkDelete(ids);
        }
        return oldLogs.length;
    }
};
